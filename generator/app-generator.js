import fs from 'fs';
import path from 'path';
import { validateAppGraph, validateContractAndIR } from './validate-contract-ir.js';

const appContractPath = process.argv[2];

if (!appContractPath) {
  console.error("Usage: node generator/app-generator.js <path-to-app-contract.json>");
  process.exit(1);
}

function loadJson(p) {
  return JSON.parse(fs.readFileSync(p, 'utf8'));
}

async function generateApp() {
  const appContract = loadJson(appContractPath);
  const stateContract = loadJson(appContract.stateContract);
  const eventsContract = loadJson(appContract.events);
  const trustContract = loadJson(appContract.trust);
  
  const componentContracts = {};
  for (const [name, p] of Object.entries(appContract.components)) {
    componentContracts[name] = loadJson(p);
  }

  console.log("Validating App Graph...");
  validateAppGraph(appContract, stateContract, trustContract, eventsContract, componentContracts);
  console.log("App Graph Validated Successfully.");

  const outDir = 'generated-examples/' + appContract.app;
  fs.mkdirSync(outDir, { recursive: true });

  generateValidators(outDir, trustContract);
  generateState(outDir, stateContract);
  
  // Reuse existing logic for the card
  console.log("Generating kanban-card...");
  const { generateComponent } = await import('./index.js');
  const cardIr = loadJson('ir/kanban-card.ir.json');
  generateComponent(componentContracts['kanban-card'], cardIr, 'contracts/kanban-card.contract.json', 'ir/kanban-card.ir.json', path.join(outDir, 'kanban-card.generated.js'));

  // Custom emitters for Column and Board orchestration
  console.log("Generating kanban-column...");
  generateColumn(outDir);

  console.log("Generating kanban-board...");
  generateBoard(outDir);

  console.log("Generating kanban-app...");
  generateAppWireup(outDir);

  console.log("Generating Tests...");
  generateTests(outDir);
}

function generateValidators(outDir, trustContract) {
  let code = `// Generated by Fe app-generator\n// Source: kanban-trust.contract.json\n\n`;
  code += `export function safeText(val) {\n  if (val == null) return '';\n  return String(val);\n}\n\n`;
  code += `export function safeClassToken(val) {\n  if (val == null) return '';\n  return String(val).toLowerCase().replace(/[^a-z0-9-]/g, '-');\n}\n\n`;
  code += `export function normalizeEnum(val, allowed) {\n  if (allowed.includes(val)) return val;\n  return allowed[0];\n}\n`;

  fs.writeFileSync(path.join(outDir, 'kanban-validators.generated.js'), code);
  console.log('Generated kanban-validators.generated.js');
}

function generateState(outDir, stateContract) {
  let code = `// Generated by Fe app-generator\n// Source: kanban-state.contract.json\n\n`;
  code += `export function getItemKey(id) { return \`kanban:item:\${id}\`; }\n`;
  code += `export function getColumnIndexKey(status) { return \`kanban:column:\${status}:index\`; }\n\n`;
  
  code += `export function applyItemEdit(map, id, patch) {\n`;
  code += `  const key = getItemKey(id);\n`;
  code += `  const existing = map.get(key) || { id };\n`;
  code += `  map.set(key, { ...existing, ...patch });\n`;
  code += `}\n\n`;

  code += `export function applyItemMove(map, id, fromStatus, toStatus, toIndex) {\n`;
  code += `  // LWW move updates item status and both index arrays\n`;
  code += `  const itemKey = getItemKey(id);\n`;
  code += `  const item = map.get(itemKey) || { id, status: fromStatus };\n`;
  code += `  map.set(itemKey, { ...item, status: toStatus });\n\n`;
  
  code += `  const fromKey = getColumnIndexKey(fromStatus);\n`;
  code += `  const toKey = getColumnIndexKey(toStatus);\n`;
  code += `  const fromList = (map.get(fromKey) || { itemIds: [] }).itemIds.filter(i => i !== id);\n`;
  code += `  map.set(fromKey, { itemIds: fromList });\n\n`;

  code += `  const toList = (map.get(toKey) || { itemIds: [] }).itemIds.filter(i => i !== id);\n`;
  code += `  toList.splice(toIndex, 0, id);\n`;
  code += `  map.set(toKey, { itemIds: toList });\n`;
  code += `}\n\n`;

  code += `export function applyItemDelete(map, id) {\n`;
  code += `  const itemKey = getItemKey(id);\n`;
  code += `  const item = map.get(itemKey);\n`;
  code += `  if (item && item.status) {\n`;
  code += `    const colKey = getColumnIndexKey(item.status);\n`;
  code += `    const list = (map.get(colKey) || { itemIds: [] }).itemIds.filter(i => i !== id);\n`;
  code += `    map.set(colKey, { itemIds: list });\n`;
  code += `  }\n`;
  code += `  map.delete(itemKey);\n`;
  code += `}\n\n`;

  code += `export function createInitialBoardState(map) {\n`;
  code += `  if (!map.get('kanban:board:metadata')) map.set('kanban:board:metadata', { title: 'Normalized Kanban', description: '' });\n`;
  code += `  if (!map.get('kanban:column:todo:index')) map.set('kanban:column:todo:index', { itemIds: [] });\n`;
  code += `  if (!map.get('kanban:column:in-progress:index')) map.set('kanban:column:in-progress:index', { itemIds: [] });\n`;
  code += `  if (!map.get('kanban:column:done:index')) map.set('kanban:column:done:index', { itemIds: [] });\n`;
  code += `}\n`;

  fs.writeFileSync(path.join(outDir, 'kanban-state.generated.js'), code);
  console.log('Generated kanban-state.generated.js');
}

function generateColumn(outDir) {
  let code = `// Generated by Fe app-generator
// Source: kanban-column.contract.json
// Do not edit for human aesthetics.

import { safeText } from './kanban-validators.generated.js';

export function createKanbanColumn(status, eventSink) {
  const root = document.createElement('div');
  root.className = \`column-root column-\${status}\`;
  
  const titleNode = document.createElement('h2');
  titleNode.className = 'column-title';
  titleNode.textContent = status.toUpperCase();
  root.appendChild(titleNode);

  const countNode = document.createElement('span');
  countNode.className = 'column-count';
  countNode.textContent = '0';
  titleNode.appendChild(countNode);

  const cardListContainer = document.createElement('div');
  cardListContainer.className = 'card-list';
  root.appendChild(cardListContainer);

  const cardInstances = new Map();

  function insertCard(id, cardInstance) {
    cardInstances.set(id, cardInstance);
    // Physically move the node to the end of this list
    cardListContainer.appendChild(cardInstance.root);
  }

  function removeCard(id) {
    const card = cardInstances.get(id);
    if (card) {
      if (card.root.parentNode === cardListContainer) {
        cardListContainer.removeChild(card.root);
      }
      cardInstances.delete(id);
    }
  }

  function reconcileOrder(itemIds) {
    countNode.textContent = String(itemIds.length);
    // Move existing nodes to match array order
    for (const id of itemIds) {
      const card = cardInstances.get(id);
      if (card) {
        cardListContainer.appendChild(card.root);
      }
    }
  }

  function dispose() {
    // Column doesn't dispose cards; Board owns them
    cardInstances.clear();
  }

  return { root, insertCard, removeCard, reconcileOrder, dispose };
}
`;
  fs.writeFileSync(path.join(outDir, 'kanban-column.generated.js'), code);
}

function generateBoard(outDir) {
  let code = `// Generated by Fe app-generator
// Source: kanban-board.contract.json
// Do not edit for human aesthetics.

import { createKanbanColumn } from './kanban-column.generated.js';
import { createKanbanCard } from './kanban-card.generated.js';

export function createKanbanBoard(initialState, eventSink) {
  const root = document.createElement('div');
  root.className = 'board-root';

  const columnsContainer = document.createElement('div');
  columnsContainer.className = 'columns-container';
  root.appendChild(columnsContainer);

  const columns = {
    'todo': createKanbanColumn('todo', eventSink),
    'in-progress': createKanbanColumn('in-progress', eventSink),
    'done': createKanbanColumn('done', eventSink)
  };

  columnsContainer.appendChild(columns['todo'].root);
  columnsContainer.appendChild(columns['in-progress'].root);
  columnsContainer.appendChild(columns['done'].root);

  const allCards = new Map();

  function patchItem(statePatch) {
    const id = statePatch.id;
    let card = allCards.get(id);
    if (!card) {
      card = createKanbanCard(statePatch, eventSink);
      allCards.set(id, card);
    } else {
      card.patch(statePatch);
    }

    if (statePatch.status && columns[statePatch.status]) {
      columns[statePatch.status].insertCard(id, card);
    }
  }

  function reconcileColumnList(status, itemIds) {
    const col = columns[status];
    if (col) {
      col.reconcileOrder(itemIds);
    }
  }

  function removeItem(id) {
    const card = allCards.get(id);
    if (card) {
      card.dispose();
      for (const col of Object.values(columns)) {
        col.removeCard(id);
      }
      allCards.delete(id);
    }
  }

  function dispose() {
    for (const card of allCards.values()) {
      card.dispose();
    }
    allCards.clear();
    for (const col of Object.values(columns)) {
      col.dispose();
    }
  }

  return { root, patchItem, reconcileColumnList, removeItem, dispose };
}
`;
  fs.writeFileSync(path.join(outDir, 'kanban-board.generated.js'), code);
}

function generateAppWireup(outDir) {
  let code = `// Generated by Fe app-generator
// Wires Shared State -> Board

import { createKanbanBoard } from './kanban-board.generated.js';
import { applyItemEdit, applyItemMove, applyItemDelete, createInitialBoardState, getItemKey, getColumnIndexKey } from './kanban-state.generated.js';
import { safeText, normalizeEnum } from './kanban-validators.generated.js';

export function createKanbanApp(sharedMap) {
  createInitialBoardState(sharedMap);

  const statuses = ['todo', 'in-progress', 'done'];

  function handleEvent(ev) {
    if (ev.type === 'kanban:item:edit') {
      const id = ev.sourceEvent.target.dataset.id;
      if (!id) return;
      applyItemEdit(sharedMap, id, { title: safeText('Edited Card') });
    } else if (ev.type === 'kanban:item:delete') {
      const id = ev.sourceEvent.target.closest('[data-node="root"]')?.dataset?.id;
      if (!id) return;
      applyItemDelete(sharedMap, id);
    } else if (ev.type === 'kanban:column:reorder') {
      // Mock reorder to bottom of next column
      const id = ev.itemId;
      const toStatus = ev.toStatus;
      applyItemMove(sharedMap, id, ev.fromStatus, toStatus, 999);
    }
  }

  const board = createKanbanBoard({}, handleEvent);

  const cleanups = [];

  // Subscribe to all items
  const observeMap = () => {
    for (const [key, val] of sharedMap.entries()) {
      if (key.startsWith('kanban:item:')) {
        board.patchItem(val);
      } else if (key.startsWith('kanban:column:')) {
        const parts = key.split(':');
        board.reconcileColumnList(parts[2], val.itemIds);
      }
    }
  };

  sharedMap.observe((changes) => {
    for (const key of changes.keys()) {
      if (key.startsWith('kanban:item:')) {
        const val = sharedMap.get(key);
        if (val) {
          board.patchItem(val);
        } else {
          board.removeItem(key.split(':')[2]);
        }
      } else if (key.startsWith('kanban:column:')) {
        const parts = key.split(':');
        const val = sharedMap.get(key);
        if (val) board.reconcileColumnList(parts[2], val.itemIds);
      }
    }
  });

  observeMap();

  return {
    root: board.root,
    dispose: () => {
      board.dispose();
      cleanups.forEach(c => c());
    }
  };
}
`;
  fs.writeFileSync(path.join(outDir, 'kanban-app.generated.js'), code);
}

function generateTests(outDir) {
  let code = `// Generated App Tests
import { describe, test, expect, vi } from 'vitest';
import { createKanbanApp } from './kanban-app.generated.js';
import { applyItemEdit, applyItemDelete, applyItemMove, getItemKey, getColumnIndexKey, createInitialBoardState } from './kanban-state.generated.js';

class MockSharedMap {
  constructor() {
    this.store = new Map();
    this.listeners = [];
  }
  get(k) { return this.store.get(k); }
  set(k, v) { this.store.set(k, v); this.notify([k]); }
  delete(k) { this.store.delete(k); this.notify([k]); }
  entries() { return this.store.entries(); }
  observe(cb) { this.listeners.push(cb); }
  notify(keys) {
    const s = new Set(keys);
    this.listeners.forEach(cb => cb(s));
  }
}

describe('Normalized Kanban App Architecture Proof', () => {

  test('item edit updates only kanban:item:<id>', () => {
    const map = new MockSharedMap();
    applyItemEdit(map, 'c1', { title: 'Test', status: 'todo' });
    expect(map.get('kanban:item:c1')).toEqual({ id: 'c1', title: 'Test', status: 'todo' });
    // Columns should not be touched
    expect(map.get('kanban:column:todo:index')).toBeUndefined();
  });

  test('moving item updates affected column indexes and item status', () => {
    const map = new MockSharedMap();
    map.set('kanban:column:todo:index', { itemIds: ['c1'] });
    map.set('kanban:column:done:index', { itemIds: [] });
    
    applyItemMove(map, 'c1', 'todo', 'done', 0);
    
    expect(map.get('kanban:item:c1').status).toBe('done');
    expect(map.get('kanban:column:todo:index').itemIds).toEqual([]);
    expect(map.get('kanban:column:done:index').itemIds).toEqual(['c1']);
  });

  test('deleting item deletes item key and removes id from column indexes', () => {
    const map = new MockSharedMap();
    map.set('kanban:item:c1', { id: 'c1', status: 'todo' });
    map.set('kanban:column:todo:index', { itemIds: ['c1', 'c2'] });

    applyItemDelete(map, 'c1');

    expect(map.get('kanban:item:c1')).toBeUndefined();
    expect(map.get('kanban:column:todo:index').itemIds).toEqual(['c2']);
  });

  test('board creates columns once', () => {
    const map = new MockSharedMap();
    const app = createKanbanApp(map);
    expect(app.root.querySelectorAll('.column-root').length).toBe(3);
  });

  test('columns move existing card nodes instead of rebuilding all cards', () => {
    const map = new MockSharedMap();
    const app = createKanbanApp(map);
    
    applyItemEdit(map, 'c1', { title: 'Test 1', status: 'todo' });
    applyItemEdit(map, 'c2', { title: 'Test 2', status: 'todo' });
    map.set('kanban:column:todo:index', { itemIds: ['c1', 'c2'] });

    const firstCard = app.root.querySelector('.column-todo .card-list').firstElementChild;
    
    // Reorder
    map.set('kanban:column:todo:index', { itemIds: ['c2', 'c1'] });
    
    const lastCard = app.root.querySelector('.column-todo .card-list').lastElementChild;
    expect(firstCard).toBe(lastCard); // Same DOM node
  });

  test('board dispose unsubscribes from state and disposes cards', () => {
    const map = new MockSharedMap();
    const app = createKanbanApp(map);
    applyItemEdit(map, 'c1', { title: 'Test 1', status: 'todo' });
    
    expect(app.root.querySelector('[data-id="c1"]')).toBeTruthy();
    app.dispose();

    // After dispose, state patches shouldn't throw errors
    applyItemEdit(map, 'c1', { title: 'Test 2', status: 'todo' });
    // Root shouldn't update
    expect(app.root.querySelector('.card-title').textContent).not.toBe('Test 2');
  });

});
`;
  fs.writeFileSync(path.join(outDir, 'normalized-kanban.generated.test.js'), code);
}

